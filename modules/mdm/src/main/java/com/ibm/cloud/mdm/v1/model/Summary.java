package com.ibm.cloud.mdm.v1.model;

import com.google.gson.annotations.SerializedName;
import com.ibm.cloud.sdk.core.service.model.GenericModel;

/**
 * The summary of statistics of the match process results.
 */
public class Summary extends GenericModel {

  @SerializedName("entities_count")
  protected Long entitiesCount;
  @SerializedName("singleton_count")
  protected Long singletonCount;
  @SerializedName("distinct_sources")
  protected Long distinctSources;
  @SerializedName("data_assets")
  protected Long dataAssets;
  @SerializedName("total_records")
  protected Long totalRecords;

  /**
   * Gets the entitiesCount.
   *
   * The total number of entities generated by the match process.
   *
   * @return the entitiesCount
   */
  public Long getEntitiesCount() {
    return entitiesCount;
  }

  /**
   * Gets the singletonCount.
   *
   * The count of entities having size of one.
   *
   * @return the singletonCount
   */
  public Long getSingletonCount() {
    return singletonCount;
  }

  /**
   * Gets the distinctSources.
   *
   * The number of distinct sources of data records.
   *
   * @return the distinctSources
   */
  public Long getDistinctSources() {
    return distinctSources;
  }

  /**
   * Gets the dataAssets.
   *
   * The number of data assets processed by the match process.
   *
   * @return the dataAssets
   */
  public Long getDataAssets() {
    return dataAssets;
  }

  /**
   * Gets the totalRecords.
   *
   * The total number of data records processed by the match process.
   *
   * @return the totalRecords
   */
  public Long getTotalRecords() {
    return totalRecords;
  }
}

